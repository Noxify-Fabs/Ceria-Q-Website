// Get story part based on URL parameter or default
function getStoryPart(partNumber) {
    return storyData[partNumber] || storyData[1];
}

// Get specific page from story part
function getPage(partNumber, pageNumber) {
    const story = storyData[partNumber];
    if (story && story.pages && story.pages[pageNumber - 1]) {
        return story.pages[pageNumber - 1];
    }
    return null;
}

// Get total pages for a part
function getTotalPages(partNumber) {
    const story = storyData[partNumber];
    return story ? story.totalPages : 10;
}

// Get all story metadata
function getAllStories() {
    const stories = {};
    for (let part in storyData) {
        stories[part] = {
            title: storyData[part].title,
            description: storyData[part].description,
            totalPages: storyData[part].totalPages
        };
    }
    return stories;
}

